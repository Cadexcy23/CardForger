void Mapper::update()
{
	
		
	//go thru each entity
	for (int i = 0; i < activeEntities.size(); i++)
	{
		//run AI
		activeEntities[i].AI(i);

		if (Controller::FPSLock)
		{
			//lower rotation if its over 360
			if (activeEntities[i].rotation >= 360)
			{
				activeEntities[i].rotation -= 360;
			}
			//raise rotation if its under 0
			if (activeEntities[i].rotation <= 0)
			{
				activeEntities[i].rotation += 360;
			}

			//apply vel to entities 
			activeEntities[i].xPos += activeEntities[i].xVel;
			activeEntities[i].yPos += activeEntities[i].yVel;

			//apply friction to entities 
			activeEntities[i].xVel *= .85;
			activeEntities[i].yVel *= .85;

			//if not really moving bring to full stop
			if (abs(activeEntities[i].xVel) < 0.05)
			{
				activeEntities[i].xVel = 0;
			}
			if (abs(activeEntities[i].yVel) < 0.05)
			{
				activeEntities[i].yVel = 0;
			}

			//collision detection
			if (!groundCollisionCheck(activeEntities[i].xPos, activeEntities[i].yPos, activeEntities[i].size)
				|| !wallCollisionCheck(activeEntities[i].xPos, activeEntities[i].yPos, activeEntities[i].size))
			{
				Artist::pos openPos = findClosestOppening(activeEntities[i].xPos, activeEntities[i].yPos, activeEntities[i].size);
				activeEntities[i].xPos = openPos.x;
				activeEntities[i].yPos = openPos.y;
				//activeEntities[i].xVel *= .9;
				//activeEntities[i].yVel *= .9;
			}

			//drain/regen stamina
			//only drain stamina every X updates
			if (staminaDrainCount >= 60) //NEEDS REPLACED LATER
			{
				staminaDrainCount = 0;//reset counter

				if (abs(activeEntities[i].xVel) + abs(activeEntities[i].yVel) > 1)
				{
					if (activeEntities[i].running)//Running
					{
						activeEntities[i].stamina.current -= 5;
						if (activeEntities[i].stamina.current <= 0)//Out of stamina
						{
							activeEntities[i].stamina.current = 0;
							stopRunning(i);
						}
					}
					else//Walking
					{
						activeEntities[i].stamina.current += 1;
						if (activeEntities[i].stamina.current > activeEntities[i].stamina.max)
						{
							activeEntities[i].stamina.current = activeEntities[i].stamina.max;
						}
					}
				}
				else//Standing still
				{
					activeEntities[i].stamina.current += 2;
					if (activeEntities[i].stamina.current > activeEntities[i].stamina.max)
					{
						activeEntities[i].stamina.current = activeEntities[i].stamina.max;
					}
				}

			}
			else
			{
				staminaDrainCount++;
			}

		}
	}
}