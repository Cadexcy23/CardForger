void generateEntPaths(int entID, int extraRoom)//MAYBE MAKE STOP IF IT FINDS PATH?? for proformance
{
	Pathfinder pathfinder;

	//determine if we can get to our destination, and find the path to get there
	//construct a node map from the actual map with size based on distance with some breathing room
	std::vector<std::vector<bool>> nodeMapToCheck;
	int newX = abs(int(Mapper::activeEntities[entID].xPos) / 64 - Mapper::activeEntities[entID].destination.x / 64) + extraRoom * 2 + 1;
	int newY = abs(int(Mapper::activeEntities[entID].yPos) / 64 - Mapper::activeEntities[entID].destination.y / 64) + extraRoom * 2 + 1;
	//int newX = abs(Mapper::activeEntities[entID].destination.x / 64 - Mapper::activeEntities[entID].xPos / 64) + extraRoom * 2 + 1;
	//int newY = abs(Mapper::activeEntities[entID].destination.y / 64 - Mapper::activeEntities[entID].yPos / 64) + extraRoom * 2 + 1;
	nodeMapToCheck.resize(newX);
	for (int x = 0; x < newX; x++)
	{
		nodeMapToCheck[x].resize(newY);
	}
	//fill the nodeMap with info from the map
	//determine where the top left is relative to the map
	Artist::pos topLeft;
	if (Mapper::activeEntities[entID].xPos > Mapper::activeEntities[entID].destination.x) //if the ent is farther left than the dest then topLeft should be based of ent
	{
		topLeft.x = int(Mapper::activeEntities[entID].xPos) / 64 - newX + extraRoom + 1;
	}
	else
	{
		topLeft.x = int(Mapper::activeEntities[entID].destination.x) / 64 - newX + extraRoom + 1;
	}
	if (Mapper::activeEntities[entID].yPos > Mapper::activeEntities[entID].destination.y) //if the ent is farther up than the dest then topLeft should be based of ent
	{
		topLeft.y = int(Mapper::activeEntities[entID].yPos) / 64 - newY + extraRoom + 1;
	}
	else
	{
		topLeft.y = int(Mapper::activeEntities[entID].destination.y) / 64 - newY + extraRoom + 1;
	}
	//go through each entry of nodes and check if its walkable
	for (int x = 0; x < nodeMapToCheck.size(); x++)
	{
		for (int y = 0; y < nodeMapToCheck[x].size(); y++)//ALSO NEED TO ADD A WAY TO CHECK WALLS
		{
			if (x + topLeft.x >= 0 && x + topLeft.x < Mapper::activeMap.tiles.size() && y + topLeft.y >= 0 && y + topLeft.y < Mapper::activeMap.tiles[0].size())
			{
				if (Mapper::activeMap.tiles[x + topLeft.x][y + topLeft.y].ground->walkable)
				{
					nodeMapToCheck[x][y] = true;
				}
				else
				{
					nodeMapToCheck[x][y] = false;
				}
			}
			else
			{
				nodeMapToCheck[x][y] = false;
			}
		}
	}
	//run the pathfinder to get a grid of where we can go and how to get to those places
	std::vector<std::vector<Artist::pos>> paths;
	std::vector<std::vector<bool>> visitable;
	Artist::pos start = { int(Mapper::activeEntities[entID].xPos) / 64 - topLeft.x, int(Mapper::activeEntities[entID].yPos) / 64 - topLeft.y };
	visitable = pathfinder.findPaths(nodeMapToCheck, start, &paths);

	//save the paths to the ent
	Mapper::activeEntities[entID].visitable = visitable;
	Mapper::activeEntities[entID].paths = paths;
	Mapper::activeEntities[entID].pathsMapPos = topLeft;
}

