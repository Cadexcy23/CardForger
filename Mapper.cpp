Artist::pos findClosestOppening(int xPos, int yPos, int size)//mayeb rework so we dont have the loops here since the collision code does that if we pass size
{
	//chance direction each time we fail to find a spot and when we look back to 0 add one to distance
	int direction = 0;
	int distance = 1;
	bool looking = true;
	while (looking)
	{
		//holds if this check is empty so far
		bool open = true;
		//go thru every unit of the entity
		for (int x = 0; x < size; x++)
		{
			for (int y = 0; y < size; y++)
			{
				//offset up and left half of size
				int offsetX = xPos + x - size / 2;
				int offsetY = yPos + y - size / 2;
				//offset in the direction and distance we are checking
				switch (direction)
				{
				case 0:
					offsetY -= distance;
					break;
				case 1:
					offsetY += distance;
					break;
				case 2:
					offsetX -= distance;
					break;
				case 3:
					offsetX += distance;
					break;
				case 4:
					offsetY -= distance;
					offsetX -= distance;
					break;
				case 5:
					offsetY -= distance;
					offsetX += distance;
					break;
				case 6:
					offsetY += distance;
					offsetX -= distance;
					break;
				case 7:
					offsetY += distance;
					offsetX += distance;
					break;
				}
				//check tile that pos is on for collision
				if (!groundCollisionCheck(offsetX, offsetY, 1) || !wallCollisionCheck(offsetX, offsetY, 1))
				{
					//flag that there is collision here
					open = false;
					//end search
					x = size;
					y = size;
				}
			}
		}
		//if there was no collision detected
		if (open)
		{
			//record the possition that was clear
			Artist::pos returnPos = { xPos, yPos };
			switch (direction)
			{
			case 0:
				returnPos.y -= distance;
				break;
			case 1:
				returnPos.y += distance;
				break;
			case 2:
				returnPos.x -= distance;
				break;
			case 3:
				returnPos.x += distance;
				break;
			case 4:
				returnPos.y -= distance;
				returnPos.x -= distance;
				break;
			case 5:
				returnPos.y -= distance;
				returnPos.x += distance;
				break;
			case 6:
				returnPos.y += distance;
				returnPos.x -= distance;
				break;
			case 7:
				returnPos.y += distance;
				returnPos.x += distance;
				break;
			}
			return returnPos;
		}
		//if it wasnt open
		else
		{
			//check next direction
			direction++;
			//if we cheacked each dir then reset to first and increase distance
			if (direction == 8)
			{
				direction = 0;
				distance++;
			}
		}
	}
}

