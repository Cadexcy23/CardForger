bool groundCollisionCheck(int xPos, int yPos, int size)
{
	//flag for if we collide
	bool open = true;
	//check each possition of the entity to see if any tiles it is on is not walkable
	for (int x = 0; x < size; x++)
	{
		for (int y = 0; y < size; y++)
		{
			//offset up and left half of size
			int offsetX = xPos + x - size / 2;
			int offsetY = yPos + y - size / 2;
			if (!Mapper::activeMap.tiles[offsetX / 64][offsetY / 64].ground->walkable)
			{
				open = false;
				//end search
				x = size;
				y = size;
			}
		}
	}
	return open;
}

